document.querySelector('#project_1').addEventListener("mouseenter", () => {
    gsap.to("#project_1", {
        duration: 0.1,
        opacity: 1,
        ease: Expo.easeInOut
    })

    gsap.to("#cacti_bg", {
        duration: 0.5,
        autoAlpha: 1,
        ease: Expo.easeInOut
    })
})

document.querySelector('#project_1').addEventListener("mouseleave", () => {
    gsap.to("#project_1", {
        duration: 0.1,
        opacity: 0.2,
        ease: Expo.easeInOut
    })

    gsap.to("#cacti_bg", {
        duration: 0.5,
        autoAlpha: 0,
        ease: Expo.easeInOut
    })
})

